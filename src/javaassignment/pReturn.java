    private void checkDueDate(){
         DateTimeFormatter dateFormat = DateTimeFormat.forPattern("dd/MM/yyyy");
           
            LocalDate currentDate = LocalDate.parse(txtReturnDate.getText(),dateFormat );
            
            LocalDate dueDate = LocalDate.parse(txtDueDate.getText(),dateFormat );
            
            //int datediff = Days.daysBetween(currentDate, dueDate).getDays();
            int datediff = Days.daysBetween(dueDate, currentDate).getDays();
            
            if (datediff < 0 ){
                txtOverdueBy.setText("0");
                 btnPayFine.setEnabled(false);
                 btnReturn.setEnabled(true);
            
            } else {
                txtOverdueBy.setText(String.valueOf(datediff));
                 btnPayFine.setEnabled(true);
                 
               FineAmount=datediff*1;
               txtFine.setText(Integer.toString(FineAmount));
            }
    }
    
