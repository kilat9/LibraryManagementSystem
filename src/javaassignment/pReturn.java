    int fine, payFine, change;
    private void PayFine(){
        fine = 0;
        payFine = 0;
        change = 0;
        try{
        if (!txtFine.getText().equals("")){
            btnPayFine.setEnabled(true);
            fine = Integer.parseInt(txtFine.getText());
            payFine = Integer.parseInt(txtPayFine.getText());
            
            if (payFine < fine){
                JOptionPane.showMessageDialog(null, " Insufficient amount to pay fine", "Insufficient funds!", JOptionPane.ERROR_MESSAGE);
                txtPayFine.setText("");
            } else{
                change = payFine - fine;
                JOptionPane.showMessageDialog(null, "Payment succesful. The change is " + change);
                btnReturn.setEnabled(true);
            }
        } else {
            btnPayFine.setEnabled(false);
            
        }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Payment cannot be empty!");
        }
    }
    
    
     
